using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Player : MonoBehaviour
{
    // 컴포넌트 참조
    Rigidbody2D rigid;
    SpriteRenderer sprite;
    Animator animator;
    AudioSource BGM;

    // 오브젝트 참조
    public GameObject settingUI;
    bool isSetOn = false;

    // 내부값 위치
    public Vector3 CurPos = new Vector3(0, 0, 0);

    // 움직임 제어 관리
    public bool Movable = true;
    public bool Settable = false;
    public float speed = 0.1f;

    public float setTime = 10;

    // Start is called before the first frame update
    void Start()
    {
        rigid = GetComponent<Rigidbody2D>();
        sprite = GetComponentInChildren<SpriteRenderer>();
        animator = GetComponentInChildren<Animator>();
        BGM = GameObject.Find("BGM").GetComponent<AudioSource>();

        settingUI.SetActive(false);

        // 스테이지 별
        // 캐릭터 초기 위치 설정
        switch(MainGame.instance.stageNum)
        {
            case 1:
                break;
            case 2:
                break;
            case 3:
                break;
            case 4:
                break;
            default:
                break;
        }
    }

    // Update is called once per frame
    void Update()
    {
        // 움직임 제어
        if (Movable)
        {
            Move();
        }

        // 시작 후 설정창
        if (Input.GetKeyDown(KeyCode.Escape) && MainGame.instance.isStart)
        {
            OnSetting();
        }

    }

    private void Move()
    {
        // 이동
        if (Input.GetKeyDown(KeyCode.LeftArrow))
        {
            // 좌우 반전
            sprite.flipX = false;
            Head(Vector3.left);

        }
        if (Input.GetKeyDown(KeyCode.RightArrow))
        {
            // 좌우 반전
            sprite.flipX = true;
            Head(Vector3.right);
        }
        if (Input.GetKeyDown(KeyCode.UpArrow))
        {
            Head(Vector3.up);
        }
        if (Input.GetKeyDown(KeyCode.DownArrow))
        {
            Head(Vector3.down);
        }

        // 캐릭터 좌표 이동
        transform.localPosition = Vector3.Lerp(transform.localPosition, CurPos, speed);
        if (transform.localPosition == CurPos)
        {
            transform.localPosition = CurPos;
        }
    }

    public void Head(Vector3 _head)
    {
        // 벽 이동불가
        LayerMask mask = LayerMask.GetMask("Wall") | LayerMask.GetMask("Object");

        // 애니메이션
        animator.SetTrigger("Jump");

        RaycastHit2D rayHit = Physics2D.Raycast(rigid.position, _head, 1, mask);
        if (!rayHit)
        {
            CurPos += _head;
            MainGame.instance.Judge(BGM.time, CurPos.x, CurPos.y);
        }
    }

    public void OnSetting()
    {
        if (!Settable) return;
        if (!isSetOn)
        {
            // 설정창 상태 설정
            isSetOn = true;

            // 플레이어 멈춤
            Movable = false;

            // 게임 멈춤
            MainGame.instance.Stop();

            // 설정창 활성화
            settingUI.SetActive(true);
        }
        else
        {
            // 설정창 상태 해제
            isSetOn = false;

            // 플레이어 움직임
            Movable = true;

            // 노트 위치로 이동
            MainGame.instance.PlayerReposition();

            // 이어하기 호출
            MainGame.instance.Continue();

            // 설정창 비활성화
            settingUI.SetActive(false);
        }
    }

    public void OffSetting()
    {
        // 설정창 상태 해제
        isSetOn = false;

        // 플레이어 움직임
        Movable = true;

        // 설정창 비활성화
        settingUI.SetActive(false);
    }
}
